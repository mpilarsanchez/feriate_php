       <h3>01 Registración</h3>
       <p>
         Es obligatorio completar el formulario de registración en todos sus campos con datos válidos para poder utilizar los servicios que brinda Feriate.com. El futuro Usuario deberá completarlo con su información personal de manera exacta, precisa y verdadera ("Datos Personales") y asume el compromiso de actualizar los Datos Personales conforme resulte necesario. El Usuario presta expresa conformidad con que Feriate.com utilice diversos medios para identificar sus datos personales, asumiendo el Usuario la obligación de revisarlos y mantenerlos actualizados. Feriate.com NO se responsabiliza por la certeza de los Datos Personales de los Usuarios. Los Usuarios garantizan y responden, en cualquier caso, de la veracidad, exactitud, vigencia y autenticidad de sus Datos Personales.
       </p>
       <p>
         El Usuario accederá a su cuenta personal ("Cuenta") mediante el ingreso de su Apodo y clave de seguridad personal elegida ("Clave de Seguridad"). El Usuario se obliga a mantener la confidencialidad de su Clave de Seguridad.
       </p>
       <p>
         La Cuenta es personal, única e intransferible, y está prohibido que un mismo Usuario registre o posea más de una Cuenta. En caso que Feriate.com detecte distintas Cuentas que contengan datos coincidentes o relacionados, podrá cancelar, suspender o inhabilitarlas.
       </p>
       <h3>02 Publicacion de bienes</h3>
       <p>
         El Usuario deberá ofrecer a la venta los bienes y promover los servicios en las categorías y subcategorías apropiadas. Las publicaciones podrán incluir textos descriptivos, gráficos, fotografías y otros contenidos y condiciones pertinentes para la venta del bien o la promoción del servicio, siempre que no violen ninguna disposición de este acuerdo o demás políticas de Feriate.com. El bien o servicio ofrecido por el Usuario vendedor debe ser exactamente descrito en cuanto a sus condiciones y características relevantes. Se entiende y presume que mediante la inclusión del bien o servicio en Feriate.com, el Usuario acepta que tiene la intención y el derecho de vender el bien u ofrecer el servicio referido, o está facultado para ello por su titular. Asimismo, el Usuario acepta que tiene disponible el bien publicado para su entrega inmediata o el servicio para su prestación en el plazo acordado. Se establece que los precios de los bienes o servicios publicados deberán ser expresados con IVA incluido cuando corresponda la aplicación del mismo, y en moneda de curso legal.
       </p>
       <h3>03 inclusión de imágenes y fotografías </h3>
       <p>
         El usuario puede incluir imágenes y fotografías del bien o servicio ofrecido siempre que las mismas se correspondan con el mismo, salvo que se trate de bienes o productos o de servicios que por su naturaleza no permiten esa correspondencia. El usuario declara y garantiza que está facultado para incluir las imágenes y fotografías contenidas en sus publicaciones, siendo responsable por cualquier infracción a derechos de terceros.
       </p>
       <p>
         Feriate.com podrá impedir la publicación de la fotografía, e incluso del bien o servicio, si interpretara, a su exclusivo criterio, que la imagen no cumple con los presentes Términos y Condiciones.
       </p>
       <h3>04 Artículos prohibidos</h3>
       <p>
       Sólo podrán ser ingresados en las listas de bienes y servicios ofrecidos, aquellos cuya venta no se encuentre tácita o expresamente prohibida en los Términos y Condiciones Generales y demás políticas de Feriate.com o por la ley vigente. Para obtener mayor información sobre bienes o servicios prohibidos, se pueden consultar nuestras"Políticas de Artículos Prohibidos" de Feriate.com.
       </p>
       <h3>05 Privacidad de la información</h3>
       <p>
         Para utilizar los Servicios ofrecidos por Feriate.com, los Usuarios deberán facilitar determinados datos de carácter personal. Su información personal se procesa y almacena en servidores o medios magnéticos que mantienen altos estándares de seguridad y protección tanto física como tecnológica.
       </p>
       <h3>06 Obligaciones de los usuarios</h3>
       <h5>6.1 Obligaciones del comprador</h5>
       <p>
         Durante el plazo fijado por el Usuario vendedor, los Usuarios interesados realizarán ofertas de compra para los bienes y servicios que permitan la contratación en el Sitio y podrán contactar a los oferentes de las categorías Ropa y Accesorios, Muebles, Electro, Calzado y Juguetes. La oferta de venta se cierra una vez que vence el plazo de la publicación o se acaban las cantidades estipuladas por el Vendedor y la promoción del servicio culmina con el vencimiento del plazo de la publicación.
        </p>
        <h5>6.2 Obligaciones del vendedor</h5>
        <p>
          El usuario vendedor debe tener capacidad legal para vender el bien o servicio objeto de su oferta. Si el usuario vendedor ha recibido una oferta queda obligado a intentar comunicarse con el comprador y completar la operación. Solamente en casos excepcionales el vendedor podrá retractarse de la venta, tales como cuando no haya podido acordar con el usuario comprador sobre la forma de pago, de entrega o no sea posible verificar la verdadera identidad o demás información del comprador.
        </p>
